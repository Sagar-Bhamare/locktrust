const MyAccount = ({ apiData }) => {
  // ** State
  const [role, setRole] = useState('')
  const [plan, setPlan] = useState('')
  const [value, setValue] = useState('')
  const [status, setStatus] = useState('')
  const [pageSize, setPageSize] = useState(10)
  const [addUserOpen, setAddUserOpen] = useState(false)

  // ** Hooks
  const dispatch = useDispatch()
  const store = useSelector(state => state.user)
  const ability = useContext(AbilityContext)

  // Check permissions using ability
  const canRead = ability?.can('read', 'myaccount')
  const canCreate = ability?.can('create', 'myaccount')
  const canUpdate = ability?.can('update', 'myaccount')
  const canDelete = ability?.can('delete', 'myaccount')

  useEffect(() => {
    if (canRead) {
      dispatch(
        fetchData({
          role,
          status,
          q: value,
          currentPlan: plan
        })
      )
    }
  }, [dispatch, plan, role, status, value, canRead])

  const handleFilter = useCallback(val => {
    setValue(val)
  }, [])

  const handleRoleChange = useCallback(e => {
    if (canUpdate) {
      setRole(e.target.value)
    }
  }, [canUpdate])

  const handlePlanChange = useCallback(e => {
    if (canUpdate) {
      setPlan(e.target.value)
    }
  }, [canUpdate])

  const handleStatusChange = useCallback(e => {
    if (canUpdate) {
      setStatus(e.target.value)
    }
  }, [canUpdate])

  const toggleAddUserDrawer = () => {
    if (canCreate) {
      setAddUserOpen(!addUserOpen)
    }
  }

  // If user doesn't have read permission, show access denied
  if (!canRead) {
    return (
      <Box sx={{ p: 5, textAlign: 'center' }}>
        <Typography variant="h5" color="error">
          Access Denied
        </Typography>
        <Typography>You don't have permission to view this page.</Typography>
      </Box>
    )
  }

  return (
    <Grid container spacing={6.5}>
      <Grid item xs={12}>
        {apiData && (
          <Grid container spacing={6}>
            {apiData.statsHorizontalWithDetails.map((item, index) => {
              return (
                <Grid item xs={12} md={3} sm={6} key={index}>
                  <CardStatsHorizontalWithDetails {...item} />
                </Grid>
              )
            })}
          </Grid>
        )}
      </Grid>
      <Grid item xs={12}>
        <Card>
          <CardHeader title='Search Filters' />
          <CardContent>
            <Grid container spacing={6}>
              <Grid item sm={4} xs={12}>
                <FormControl fullWidth>
                  <InputLabel id='role-select'>Select Role</InputLabel>
                  <Select
                    fullWidth
                    value={role}
                    id='select-role'
                    label='Select Role'
                    labelId='role-select'
                    onChange={handleRoleChange}
                    inputProps={{ placeholder: 'Select Role' }}
                    disabled={!canUpdate}
                  >
                    <MenuItem value=''>Select Role</MenuItem>
                    <MenuItem value='admin'>Admin</MenuItem>
                    <MenuItem value='author'>Author</MenuItem>
                    <MenuItem value='editor'>Editor</MenuItem>
                    <MenuItem value='maintainer'>Maintainer</MenuItem>
                    <MenuItem value='subscriber'>Subscriber</MenuItem>
                  </Select>
                </FormControl>
              </Grid>
              <Grid item sm={4} xs={12}>
                <FormControl fullWidth>
                  <InputLabel id='plan-select'>Select Plan</InputLabel>
                  <Select
                    fullWidth
                    value={plan}
                    id='select-plan'
                    label='Select Plan'
                    labelId='plan-select'
                    onChange={handlePlanChange}
                    inputProps={{ placeholder: 'Select Plan' }}
                    disabled={!canUpdate}
                  >
                    <MenuItem value=''>Select Plan</MenuItem>
                    <MenuItem value='basic'>Basic</MenuItem>
                    <MenuItem value='company'>Company</MenuItem>
                    <MenuItem value='enterprise'>Enterprise</MenuItem>
                    <MenuItem value='team'>Team</MenuItem>
                  </Select>
                </FormControl>
              </Grid>
              <Grid item sm={4} xs={12}>
                <FormControl fullWidth>
                  <InputLabel id='status-select'>Select Status</InputLabel>
                  <Select
                    fullWidth
                    value={status}
                    id='select-status'
                    label='Select Status'
                    labelId='status-select'
                    onChange={handleStatusChange}
                    inputProps={{ placeholder: 'Select Role' }}
                    disabled={!canUpdate}
                  >
                    <MenuItem value=''>Select Status</MenuItem>
                    <MenuItem value='pending'>Pending</MenuItem>
                    <MenuItem value='active'>Active</MenuItem>
                    <MenuItem value='inactive'>Inactive</MenuItem>
                  </Select>
                </FormControl>
              </Grid>
            </Grid>
          </CardContent>
          <Divider sx={{ m: '0 !important' }} />
          
          <TableHeader 
            value={value} 
            handleFilter={handleFilter} 
            toggle={toggleAddUserDrawer}
            canCreate={canCreate}
          />
          
          <DataGrid
            autoHeight
            rowHeight={62}
            rows={store.data || []}
            columns={columns(ability)}
            pageSize={pageSize}
            disableSelectionOnClick
            rowsPerPageOptions={[10, 25, 50]}
            onPageSizeChange={newPageSize => setPageSize(newPageSize)}
            checkboxSelection={canUpdate}
            disableColumnSelector={!canUpdate}
          />
        </Card>
      </Grid>

      {canCreate && (
        <AddUserDrawer open={addUserOpen} toggle={toggleAddUserDrawer} />
      )}
    </Grid>
  )
}

export const getStaticProps = async () => {
  const res = await axios.get('/cards/statistics')
  const apiData = res.data

  return {
    props: {
      apiData
    }
  }
}

